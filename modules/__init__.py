# =============================================================================
# Adaptive Image Denoising Framework — Patent-Worthy Modular Architecture
# =============================================================================
# This package implements a self-learning, region-aware, multi-filter fusion
# denoising system with ML-based weight optimization and edge preservation
# evaluation.
#
# Modules:
#   utils               — Shared helpers, image loading, noise injection
#   noise_detection     — Automatic noise profile detection (type/intensity/distribution)
#   region_segmentation — Region-aware image segmentation (smooth/edge/texture)
#   filters             — Classical filters + adaptive region-based denoising
#   fusion              — Multi-filter weighted fusion engine
#   metrics             — Quality metrics (MSE, PSNR, SSIM, Edge Preservation Score)
#   ml_optimizer        — ML-based filter weight prediction (regression)
#   self_learning       — Self-learning feedback system for continuous improvement
